private:
    // Read first N data rows from a CSV file (skipping header)
    static std::string read_csv_summary(const std::string& path, int max_rows) {
        std::ifstream f(path);
        if (!f.is_open()) return "";
        std::stringstream ss;
        std::string line;
        int rows = 0;
        while (std::getline(f, line)) {
            ss << line << "\n";
            if (rows++ >= max_rows) break;
        }
        return ss.str();
    }
